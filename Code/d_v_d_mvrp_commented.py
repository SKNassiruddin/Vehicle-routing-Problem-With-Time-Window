"""## 🛠️ Helper Functions: Nearest Neighbor TSP & Route Storage

**What this cell does:**
Defines the core helper function used for routing throughout the notebook, and initializes the global route storage list.

**`nearest_neighbor_route(nodes, start_node)`:**
- Implements the **Nearest Neighbor heuristic** for solving the Travelling Salesman Problem (TSP)
- Starting from `start_node` (usually the hub or depot), it always moves to the **closest unvisited node** next
- Returns a list of node IDs in visit order
- Simple and fast — good enough for real-world waste collection routing

**`ROUTES = []`:**
- A global list that stores all final route results
- Each entry contains cluster_id, route sequence, total distance, and total time

**Important:**
Run this cell before any cluster-specific routing cells.
"""

ROUTES = []   # Empty list that will collect all computed routes across all clusters

# -------------------------------------------------
# Helper: Nearest Neighbor TSP
# -------------------------------------------------
def nearest_neighbor_route(nodes, start_node=0):  # Greedy heuristic: at each step, visit the nearest unvisited node
    route = [start_node]  # Start the route at the given start node (either depot or hub)
    remaining = set(nodes)  # Set of nodes not yet visited — set allows fast removal

    current = start_node
    while remaining:  # Keep visiting nodes until all are included in the route
        next_node = min(  # Pick the closest unvisited node to the current position
            remaining,
            key=lambda x: distance_df.loc[current, x]  # Distance lookup from distance matrix (O(1) per lookup)
        )
        route.append(next_node)  # Add the chosen nearest node to the route
        remaining.remove(next_node)  # Mark this node as visited by removing it from the remaining set
        current = next_node

    route.append(start_node)  # Return to the starting node to complete the circuit (closed route)
    return route  # Return the complete ordered route as a list of node IDs

# -------------------------------------------------
# Run routing for each cluster
# -------------------------------------------------
cluster_ids = sorted(df_nodes["cluster_id"].unique())
cluster_ids = [c for c in cluster_ids if c != -1]  # exclude depot

print("🚛 Running routing for clusters:", cluster_ids)

for cid in cluster_ids:

    cluster_nodes = df_nodes[df_nodes["cluster_id"] == cid]["node_id"].tolist()

    # Generate route
    route = nearest_neighbor_route(cluster_nodes, start_node=0)

    # -----------------------------
    # Distance calculation
    # -----------------------------
    total_distance = 0
    total_travel_time = 0

    for i in range(len(route) - 1):
        total_distance += distance_df.loc[route[i], route[i + 1]]
        total_travel_time += time_df.loc[route[i], route[i + 1]]

    # -----------------------------
    # Service time
    # -----------------------------
    num_stops = len(cluster_nodes)
    service_time = num_stops * SERVICE_TIME_PER_STOP_MIN

    total_time = total_travel_time + service_time

    ROUTES.append({
        "cluster_id": cid,
        "num_stops": num_stops,
        "route": route,
        "distance_km": round(total_distance, 2),
        "travel_time_min": round(total_travel_time, 1),
        "service_time_min": service_time,
        "total_time_min": round(total_time, 1),
        "time_feasible": total_time <= MAX_ROUTE_TIME_MIN
    })

# -------------------------------------------------
# Convert to DataFrame
# -------------------------------------------------
routes_df = pd.DataFrame(ROUTES)

print("\n✅ Cluster-wise Routing Summary")
display(routes_df)

