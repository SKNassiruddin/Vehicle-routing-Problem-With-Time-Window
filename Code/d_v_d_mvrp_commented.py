"""## ⚖️ CELL 6: STRICT Zero-Overload Capacity-Aware Clustering Fix

**What this cell does:**
Fixes the capacity problem from K-Means clustering. K-Means only groups by geography — it ignores vehicle weight limits. This cell re-assigns nodes so that **no cluster exceeds the big vehicle's capacity**.

**Algorithm:**
1. Start with K-Means cluster assignments
2. Check total waste in each cluster
3. If any cluster exceeds `VEHICLE_CAPACITY`, move excess nodes to neighboring clusters or create new sub-groups
4. Repeat until all clusters are within capacity (zero overload)

**Output:**
- Updated `cluster_id` for each node in `df_nodes`
- Every cluster is now guaranteed to be within capacity

**Why "STRICT":**
Unlike a soft fix, this ensures absolutely no vehicle is overloaded — even by 1 kg.
"""

