# -------------------------------------------------
def haversine_km(lat1, lon1, lat2, lon2):  # Haversine formula: calculates real-world distance (km) between two GPS points on Earth's surface
    """
    Do GPS points ke beech ka distance (km me)
    """
    lat1, lon1, lat2, lon2 = map(radians, [lat1, lon1, lat2, lon2])  # Convert degrees to radians — required because trig functions use radians
    dlat = lat2 - lat1  # Difference in latitude between the two points
    dlon = lon2 - lon1  # Difference in longitude between the two points
    a = sin(dlat/2)**2 + cos(lat1) * cos(lat2) * sin(dlon/2)**2  # Core Haversine formula: computes the central angle between two points on a sphere
    c = 2 * asin(sqrt(a))  # Convert the central angle to arc length
    return 6371 * c  # Multiply by Earth's radius (6371 km) to get distance in kilometres

