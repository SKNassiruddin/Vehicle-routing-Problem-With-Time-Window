print("🔁 Starting clustering with vehicles (K):", K)

# -------------------------------------------------
# STRICT clustering loop (no overload allowed)
# -------------------------------------------------
max_iterations = 20
iteration = 0
feasible = False

while not feasible and iteration < max_iterations:
    iteration += 1
    print(f"\n🔄 Iteration {iteration} | Trying K = {K}")

    # Run K-Means
    kmeans = KMeans(
        n_clusters=K,
        random_state=42,
        n_init=10
    )

    df_work["cluster_id"] = kmeans.fit_predict(
        df_work[["lat", "lon"]]
    )

    # Compute cluster loads
    cluster_summary = (
        df_work.groupby("cluster_id")["waste_kg"]
        .sum()
        .reset_index()
        .rename(columns={"waste_kg": "total_waste_kg"})
    )

    max_load = cluster_summary["total_waste_kg"].max()

    print("📊 Cluster loads:")
    display(cluster_summary)

    # Check STRICT capacity condition
    if max_load <= SMALL_VEHICLE_CAPACITY_KG:
        feasible = True
        print("✅ STRICT condition satisfied: No cluster overloaded")
    else:
        print(f"❌ Overload detected (max = {max_load} kg). Increasing vehicles...")
        K += 1

# -------------------------------------------------
# Final validation
# -------------------------------------------------
assert feasible, "❌ Could not find feasible clustering without overload"

# -------------------------------------------------
